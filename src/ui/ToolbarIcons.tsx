// Inline line-icons for the header toolbar, embedded so we ship no asset files.
// All render in currentColor and are decorative (aria-hidden) — they sit beside
// a text label, so the label carries the accessible name. Matches the inline-SVG
// approach of OctocatIcon.

export interface IconProps {
  size?: number;
}

function Line({ size = 16, children }: IconProps & { children: React.ReactNode }) {
  return (
    <svg
      width={size}
      height={size}
      viewBox="0 0 24 24"
      fill="none"
      stroke="currentColor"
      strokeWidth={2}
      strokeLinecap="round"
      strokeLinejoin="round"
      aria-hidden="true"
      focusable="false"
    >
      {children}
    </svg>
  );
}

